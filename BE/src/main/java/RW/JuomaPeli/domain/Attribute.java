package RW.JuomaPeli.domain;

import java.util.List;

public class Attribute {
	
	private List<String> good_attributes;
	private List<String> bad_attributes;
	
	public List<String> getGood_attributes() {
		return good_attributes;
	}
	public void setGood_attributes(List<String> good_attributes) {
		this.good_attributes = good_attributes;
	}
	public List<String> getBad_attributes() {
		return bad_attributes;
	}
	public void setBad_attributes(List<String> bad_attributes) {
		this.bad_attributes = bad_attributes;
	}
	
	

}
